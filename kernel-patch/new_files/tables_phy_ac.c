/*
 * Broadcom B43 wireless driver
 * AC-PHY init tables and the bulk write path that feeds them into the
 * chip.
 *
 * Three things live in this file:
 *
 *   1. b43_phy_ac_tbl_write_lock()/_unlock() — driver-internal helpers
 *      for bit 0x0002 of B43_PHY_AC_REG_TBL_WRITE_GATE (0x19E). Pattern
 *      verified against the wl proprietary blob. Promoted to driver-
 *      internal scope (declared in tables_phy_ac.h) for the rxgain
 *      runtime port (rxgain_phy_ac.c), the second caller after
 *      b43_phy_ac_tables_init().
 *
 *   2. b43_actab_write_bulk() — the AC-PHY equivalent of
 *      b43_httab_write_bulk() (tables_phy_ht.c). It writes TABLE_ID,
 *      then TABLE_OFFSET, then streams data via TABLE_DATA_LO (and
 *      TABLE_DATA_HI for width 32). Verified against
 *      wlc_phy_write_table_ext() at 0x557f0 in wlDSL-3580_EU.o_save:
 *      the wrapper wlc_phy_table_write_acphy() (0x3ecf4) hardcodes the
 *      four register numbers as immediates 13, 14, 15, 16.
 *
 *   3. b43_phy_ac_tables_init() — top-level init driven by a static
 *      array of descriptors. 24 descriptors total (the rev-0
 *      acphytbl_info_rev0 set extracted from the blob); all 24 are
 *      populated, with the matching acphy_<name>_rev0[] arrays
 *      defined in this same file (copied from
 *      reverse-output/acphy_tables_full.c). iq_lut_core1_rev0 is
 *      an exact duplicate of iq_lut_core0_rev0, mirroring the
 *      blob's own deduplication on this 3x3 SKU (see ROADMAP §7r.5).
 *      Ids 0x40/0x60/0x80 (width 16, off 0) intentionally appear
 *      twice in the descriptor: first as est_pwr_lut_core{0,1,2},
 *      then as papd_comp_rfpwr_tbl_core{0,1,2}, matching the blob's
 *      "second pass" which overwrites the est_pwr_lut entries with
 *      the PAPD-compensated RF-power LUT during init.
 *
 *      Each descriptor declares the core it belongs to (0..NUM_CORES-1)
 *      or TBL_SHARED for chip-wide tables. The init loop skips
 *      per-core entries whose core index is out of range for the
 *      compiled NUM_CORES, matching the chain-mask check the wl blob
 *      applies inside its own per-core loop. The
 *      TBL_TODO() macro stays defined alongside TBL_POPULATED();
 *      no current entry uses it, but the init loop will skip any
 *      entry with .data == NULL if one is added later.
 */

#include "b43.h"
#include "phy_ac.h"
#include "tables_phy_ac.h"

/**************************************************
 * PHY-table-write gate (file-local)
 **************************************************/

/*
 * The wl blob brackets every PHY-table write and every RF-control
 * override with save+set / restore of bit 0x0002 of register 0x19E.
 * ~30 strictly-paired triplets in the blob, no orphan sets. The exact
 * hardware semantic of the bit is not confirmed; the mechanical
 * sequence is unambiguous.
 */
u16 b43_phy_ac_tbl_write_lock(struct b43_wldev *dev)
{
	u16 saved = b43_phy_read(dev, B43_PHY_AC_REG_TBL_WRITE_GATE);

	b43_phy_set(dev, B43_PHY_AC_REG_TBL_WRITE_GATE,
		    B43_PHY_AC_TBL_WRITE_GATE_LOCK);
	return saved;
}

void b43_phy_ac_tbl_write_unlock(struct b43_wldev *dev, u16 saved)
{
	b43_phy_maskset(dev, B43_PHY_AC_REG_TBL_WRITE_GATE,
			(u16)~B43_PHY_AC_TBL_WRITE_GATE_LOCK,
			saved & B43_PHY_AC_TBL_WRITE_GATE_LOCK);
}

/**************************************************
 * Bulk table write
 **************************************************/

/*
 * b43_actab_write_bulk - write a contiguous run of values into one of
 * the AC-PHY's internal tables.
 *
 * @dev:    b43 device
 * @id:     PHY-table id (the value programmed into TABLE_ID, register
 *          B43_PHY_AC_TABLE_ID == 0x00d)
 * @offset: starting offset inside the table; written once into
 *          TABLE_OFFSET (register 0x00e). The chip auto-increments
 *          internally across writes to TABLE_DATA_LO, so no further
 *          OFFSET writes are needed mid-burst.
 * @width:  bits per entry. Currently 8, 16 or 32 are supported. The
 *          blob's wlc_phy_write_table_ext also handles 12/20/24/28 via
 *          a jump table at .rodata+0x787c, but those widths only show
 *          up in TX-power calibration tables that are loaded at
 *          runtime, not at init — out of scope for the MVP.
 * @len:    number of entries (NOT bytes)
 * @data:   pointer to the data array. Element type must match @width:
 *          u8 array for 8, u16 for 16, u32 for 32.
 *
 * Caller must hold the table-write gate (see
 * b43_phy_ac_tbl_write_lock()).
 */
void b43_actab_write_bulk(struct b43_wldev *dev,
			  u16 id, u16 offset, u8 width,
			  size_t len, const void *data)
{
	size_t i;

	b43_phy_write(dev, B43_PHY_AC_TABLE_ID, id);
	b43_phy_write(dev, B43_PHY_AC_TABLE_OFFSET, offset);

	switch (width) {
	case 8: {
		const u8 *p = data;

		for (i = 0; i < len; i++)
			b43_phy_write(dev, B43_PHY_AC_TABLE_DATA_LO, p[i]);
		break;
	}
	case 16: {
		const u16 *p = data;

		for (i = 0; i < len; i++)
			b43_phy_write(dev, B43_PHY_AC_TABLE_DATA_LO, p[i]);
		break;
	}
	case 32: {
		const u32 *p = data;

		for (i = 0; i < len; i++) {
			b43_phy_write(dev, B43_PHY_AC_TABLE_DATA_HI,
				      (u16)(p[i] >> 16));
			b43_phy_write(dev, B43_PHY_AC_TABLE_DATA_LO,
				      (u16)(p[i] & 0xffff));
		}
		break;
	}
	default:
		b43warn(dev->wl,
			"actab_write_bulk: unsupported width %u (id=0x%x len=%zu)\n",
			width, id, len);
		break;
	}
}

/**************************************************
 * Table descriptors and data
 **************************************************/

/*
 * Sentinel for descriptors that are not per-core (one instance for the
 * whole chip). u8 0xff is well outside any plausible core index — real
 * AC-PHY chips top out at 3 cores per the wl blob's PHY reg 0x0B mask
 * (& 0x07), so 0xff cannot collide.
 */
#define TBL_SHARED		0xff

struct b43_phy_ac_table_desc {
	u16 id;
	u16 offset;
	u8 width;
	u8 core;		/* 0..NUM_CORES-1, or TBL_SHARED */
	size_t len;
	const void *data;	/* NULL = not yet populated */
	const char *name;	/* for dmesg on skip */
};

