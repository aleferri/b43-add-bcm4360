/**************************************************
 * Init
 **************************************************/

void b43_phy_ac_tables_init(struct b43_wldev *dev)
{
	const struct b43_phy_ac_table_desc *t;
	unsigned int populated = 0, skipped_todo = 0, skipped_core = 0;
	u16 saved;
	size_t i;

	saved = b43_phy_ac_tbl_write_lock(dev);

	for (i = 0; i < ARRAY_SIZE(b43_phy_ac_tables_rev0); i++) {
		t = &b43_phy_ac_tables_rev0[i];

		/*
		 * Per-core gate. The wl blob applies a chain-mask check
		 * around its own per-core loop; we mirror
		 * the conservative side here against the compile-time
		 * NUM_CORES. When num_cores becomes a runtime field on
		 * struct b43_phy_ac, replace
		 * B43_PHY_AC_NUM_CORES with dev->phy.ac->num_cores.
		 */
		if (t->core != TBL_SHARED && t->core >= B43_PHY_AC_NUM_CORES) {
			skipped_core++;
			continue;
		}
		if (!t->data) {
			skipped_todo++;
			continue;
		}
		b43_actab_write_bulk(dev, t->id, t->offset, t->width,
				     t->len, t->data);
		populated++;
	}

	b43_phy_ac_tbl_write_unlock(dev, saved);

	b43dbg(dev->wl,
	       "phy-ac: tables_init wrote %u/%zu tables (%u not yet populated, %u skipped: core out of range)\n",
	       populated, ARRAY_SIZE(b43_phy_ac_tables_rev0),
	       skipped_todo, skipped_core);
}
